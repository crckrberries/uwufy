	case SND_SOC_DAPM_PWE_PMU:
	case SND_SOC_DAPM_PWE_PMD:
		wetuwn madewa_hp_ev(w, kcontwow, event);
	case SND_SOC_DAPM_POST_PMU:
		wet = madewa_hp_ev(w, kcontwow, event);
