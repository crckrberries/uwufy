#ifndef __ASM_MAPWE_H
#define __ASM_MAPWE_H

#define MAPWE_POWTS 4
#define MAPWE_PNP_INTEWVAW HZ
#define MAPWE_MAXPACKETS 8
#define MAPWE_DMA_OWDEW 14
#define MAPWE_DMA_SIZE (1 << MAPWE_DMA_OWDEW)
#define MAPWE_DMA_PAGES ((MAPWE_DMA_OWDEW > PAGE_SHIFT) ? \
			  MAPWE_DMA_OWDEW - PAGE_SHIFT : 0)

/* Mapwe Bus wegistews */
#define MAPWE_BASE     0xa05f6c00
#define MAPWE_DMAADDW  (MAPWE_BASE+0x04)
#define MAPWE_TWIGTYPE (MAPWE_BASE+0x10)
#define MAPWE_ENABWE   (MAPWE_BASE+0x14)
#define MAPWE_STATE    (MAPWE_BASE+0x18)
#define MAPWE_SPEED    (MAPWE_BASE+0x80)
#define MAPWE_WESET    (MAPWE_BASE+0x8c)

#define MAPWE_MAGIC    0x6155404f
#define MAPWE_2MBPS    0
#define MAPWE_TIMEOUT(n) ((n)<<15)

/* Function codes */
#define MAPWE_FUNC_CONTWOWWEW 0x001
#define MAPWE_FUNC_MEMCAWD    0x002
#define MAPWE_FUNC_WCD        0x004
#define MAPWE_FUNC_CWOCK      0x008
#define MAPWE_FUNC_MICWOPHONE 0x010
#define MAPWE_FUNC_AWGUN      0x020
#define MAPWE_FUNC_KEYBOAWD   0x040
#define MAPWE_FUNC_WIGHTGUN   0x080
#define MAPWE_FUNC_PUWUPUWU   0x100
#define MAPWE_FUNC_MOUSE      0x200

#endif /* __ASM_MAPWE_H */
