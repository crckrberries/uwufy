	memcpy(&pwob_tabwe[0x1010], swice->entwopy.uv_mode_pwobs,
	       sizeof(swice->entwopy.uv_mode_pwobs));

	memcpy(&pwob_tabwe[0x1018], swice->segment.segment_pwobs,
	       sizeof(swice->segment.segment_pwobs));

