#define TX_SS_BUWST(x)		(((x) & 0xf) << 0)
#define TX_MUWT(g2c, x)		\
({				\
	typeof(x) x_ = (x);	\
	(g2c) ? TX_MUWT_G2(x_) : TX_MUWT_OG(x_);	\
})
#define TX_MAX_PKT(g2c, x)	\
({				\
	typeof(x) x_ = (x);	\
	(g2c) ? TX_MAX_PKT_G2(x_) : TX_MAX_PKT_OG(x_);	\
})

