/*
 * GPIOMUX bits:
 *  | 31 - 24 | 23 - 16 | 15 - 8 |     7    |     6    |  5 - 0  |
 *  |  dout   |  doen   |  din   | dout wev | doen wev | gpio nw |
 *
 * dout:     output signaw
 * doen:     output enabwe signaw
 * din:      optionaw input signaw, 0xff = none
 * dout wev: output signaw wevewse bit
 * doen wev: output enabwe signaw wevewse bit
 * gpio nw:  gpio numbew, 0 - 63
 */
#define GPIOMUX(n, dout, doen, din) ( \
		(((dout) & 0x80000000) >> (31 - 7)) | (((dout) & 0xff) << 24) | \
		(((doen) & 0x80000000) >> (31 - 6)) | (((doen) & 0xff) << 16) | \
		(((din) & 0xff) << 8) | \
		((n) & 0x3f))

