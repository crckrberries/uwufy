#define SDEI_1_0_FN_SDEI_EVENT_COMPWETE_AND_WESUME	SDEI_1_0_FN(0x06)
#define SDEI_1_0_FN_SDEI_EVENT_UNWEGISTEW		SDEI_1_0_FN(0x07)
