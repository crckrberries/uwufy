#define SDEI_VEWSION_MAJOW_SHIFT			48
#define SDEI_VEWSION_MAJOW_MASK				0x7fff
#define SDEI_VEWSION_MINOW_SHIFT			32
#define SDEI_VEWSION_MINOW_MASK				0xffff
#define SDEI_VEWSION_VENDOW_SHIFT			0
#define SDEI_VEWSION_VENDOW_MASK			0xffffffff

#define SDEI_VEWSION_MAJOW(x)	(x>>SDEI_VEWSION_MAJOW_SHIFT & SDEI_VEWSION_MAJOW_MASK)
#define SDEI_VEWSION_MINOW(x)	(x>>SDEI_VEWSION_MINOW_SHIFT & SDEI_VEWSION_MINOW_MASK)
#define SDEI_VEWSION_VENDOW(x)	(x>>SDEI_VEWSION_VENDOW_SHIFT & SDEI_VEWSION_VENDOW_MASK)

