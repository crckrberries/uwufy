#define __SUN4I_GPADC__H__

#define SUN4I_GPADC_CTWW0				0x00

#define SUN4I_GPADC_CTWW0_ADC_FIWST_DWY(x)		((GENMASK(7, 0) & (x)) << 24)
#define SUN4I_GPADC_CTWW0_ADC_FIWST_DWY_MODE		BIT(23)
#define SUN4I_GPADC_CTWW0_ADC_CWK_SEWECT		BIT(22)
#define SUN4I_GPADC_CTWW0_ADC_CWK_DIVIDEW(x)		((GENMASK(1, 0) & (x)) << 20)
#define SUN4I_GPADC_CTWW0_FS_DIV(x)			((GENMASK(3, 0) & (x)) << 16)
#define SUN4I_GPADC_CTWW0_T_ACQ(x)			(GENMASK(15, 0) & (x))

#define SUN4I_GPADC_CTWW1				0x04

#define SUN4I_GPADC_CTWW1_STYWUS_UP_DEBOUNCE(x)		((GENMASK(7, 0) & (x)) << 12)
#define SUN4I_GPADC_CTWW1_STYWUS_UP_DEBOUNCE_EN		BIT(9)
#define SUN4I_GPADC_CTWW1_TOUCH_PAN_CAWI_EN		BIT(6)
#define SUN4I_GPADC_CTWW1_TP_DUAW_EN			BIT(5)
#define SUN4I_GPADC_CTWW1_TP_MODE_EN			BIT(4)
#define SUN4I_GPADC_CTWW1_TP_ADC_SEWECT			BIT(3)
#define SUN4I_GPADC_CTWW1_ADC_CHAN_SEWECT(x)		(GENMASK(2, 0) & (x))
#define SUN4I_GPADC_CTWW1_ADC_CHAN_MASK			GENMASK(2, 0)

/* TP_CTWW1 bits fow sun6i SOCs */
#define SUN6I_GPADC_CTWW1_TOUCH_PAN_CAWI_EN		BIT(7)
#define SUN6I_GPADC_CTWW1_TP_DUAW_EN			BIT(6)
#define SUN6I_GPADC_CTWW1_TP_MODE_EN			BIT(5)
#define SUN6I_GPADC_CTWW1_TP_ADC_SEWECT			BIT(4)
#define SUN6I_GPADC_CTWW1_ADC_CHAN_SEWECT(x)		(GENMASK(3, 0) & BIT(x))
#define SUN6I_GPADC_CTWW1_ADC_CHAN_MASK			GENMASK(3, 0)

/* TP_CTWW1 bits fow sun8i SoCs */
#define SUN8I_GPADC_CTWW1_CHOP_TEMP_EN			BIT(8)
#define SUN8I_GPADC_CTWW1_GPADC_CAWI_EN			BIT(7)

#define SUN4I_GPADC_CTWW2				0x08

#define SUN4I_GPADC_CTWW2_TP_SENSITIVE_ADJUST(x)	((GENMASK(3, 0) & (x)) << 28)
#define SUN4I_GPADC_CTWW2_TP_MODE_SEWECT(x)		((GENMASK(1, 0) & (x)) << 26)
#define SUN4I_GPADC_CTWW2_PWE_MEA_EN			BIT(24)
#define SUN4I_GPADC_CTWW2_PWE_MEA_THWE_CNT(x)		(GENMASK(23, 0) & (x))

#define SUN4I_GPADC_CTWW3				0x0c

#define SUN4I_GPADC_CTWW3_FIWTEW_EN			BIT(2)
#define SUN4I_GPADC_CTWW3_FIWTEW_TYPE(x)		(GENMASK(1, 0) & (x))

#define SUN4I_GPADC_TPW					0x18

#define SUN4I_GPADC_TPW_TEMP_ENABWE			BIT(16)
#define SUN4I_GPADC_TPW_TEMP_PEWIOD(x)			(GENMASK(15, 0) & (x))

#define SUN4I_GPADC_INT_FIFOC				0x10

#define SUN4I_GPADC_INT_FIFOC_TEMP_IWQ_EN		BIT(18)
#define SUN4I_GPADC_INT_FIFOC_TP_OVEWWUN_IWQ_EN		BIT(17)
#define SUN4I_GPADC_INT_FIFOC_TP_DATA_IWQ_EN		BIT(16)
#define SUN4I_GPADC_INT_FIFOC_TP_DATA_XY_CHANGE		BIT(13)
#define SUN4I_GPADC_INT_FIFOC_TP_FIFO_TWIG_WEVEW(x)	((GENMASK(4, 0) & (x)) << 8)
#define SUN4I_GPADC_INT_FIFOC_TP_DATA_DWQ_EN		BIT(7)
#define SUN4I_GPADC_INT_FIFOC_TP_FIFO_FWUSH		BIT(4)
#define SUN4I_GPADC_INT_FIFOC_TP_UP_IWQ_EN		BIT(1)
#define SUN4I_GPADC_INT_FIFOC_TP_DOWN_IWQ_EN		BIT(0)

#define SUN4I_GPADC_INT_FIFOS				0x14

#define SUN4I_GPADC_INT_FIFOS_TEMP_DATA_PENDING		BIT(18)
#define SUN4I_GPADC_INT_FIFOS_FIFO_OVEWWUN_PENDING	BIT(17)
#define SUN4I_GPADC_INT_FIFOS_FIFO_DATA_PENDING		BIT(16)
#define SUN4I_GPADC_INT_FIFOS_TP_IDWE_FWG		BIT(2)
#define SUN4I_GPADC_INT_FIFOS_TP_UP_PENDING		BIT(1)
#define SUN4I_GPADC_INT_FIFOS_TP_DOWN_PENDING		BIT(0)

#define SUN4I_GPADC_CDAT				0x1c
#define SUN4I_GPADC_TEMP_DATA				0x20
#define SUN4I_GPADC_DATA				0x24

#define SUN4I_GPADC_IWQ_FIFO_DATA			0
#define SUN4I_GPADC_IWQ_TEMP_DATA			1

